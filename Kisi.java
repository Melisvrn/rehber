public class Kisi {
    private int id;
    private String ad ;
    private String soyad;
    private long numara;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getAd() {
        return ad;
    }
    public void setAd(String ad) {
        this.ad = ad;
    }
    public String getSoyad() {
        return soyad;
    }
    public void setSoyad(String soyad) {
        this.soyad = soyad;
    }
    public long getNumara() {
        return numara;
    }
    public void setNumara(long numara) {
        this.numara = numara;
    }
}
